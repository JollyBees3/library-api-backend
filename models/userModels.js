import { Schema, model } from "mongoose";

const signUpSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export const signUpModel = model("signUp", signUpSchema);
